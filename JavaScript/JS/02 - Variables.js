//    Es posible declarar una variable por medio de la palabra reservada "let" y "var"
let nombre = "Jair";
var apellido = "Cano";
console.log(nombre + apellido);

//    Como las variables son identificadas por el interprete, es posible modificar su valor
let a = "cadena";
console.log(a);
a = 123;
console.log(a);
a = a + 2;
console.log(a);
