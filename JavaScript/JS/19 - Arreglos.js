/* -------------------------------------- 19 - Arreglos -------------------------------------------

   Es una colección de elementos o datos sin orden o tipo.

   - Almacenan cualquier dato de primera clase.
   - La longitud es igual a la cantidad de elementos.
   - Las posiciones inician a contar desde 0,
   - La ultima posicion es la longitud menos 1.

*/

//    Declaración de un arreglo
let arreglo = [1, 34, 7, 293, 45];

//    Imprimir los elementos de un arreglo
for (let i=0; i>arreglo.length; i++){
   console.log(arreglo[i]);
};
